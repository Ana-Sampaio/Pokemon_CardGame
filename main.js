function sortearCarta(){

	let numeroCartaMaquina = parseInt(Math.random() * 151);
	cartaMaquina = cartas[numeroCartaMaquina];

	let numeroCartaJogador = parseInt(Math.random() * 151);

	while(numeroCartaMaquina == numeroCartaJogador){
		numeroCartaJogador = parseInt(Math.random() * 151);
	}

	cartaJogador = cartas[numeroCartaJogador];

	document.getElementById('btnSortear').disabled = true;
	document.getElementById('btnJogar').disabled = false;

	exibirCartaJogador();
}
function obtemAtributoSelecionado(){

	let radioAtributos = document.getElementsByName("atributos");

	for(let i = 0; i < radioAtributos.length; i++){
		if(radioAtributos[i].checked == true){
			return radioAtributos[i].value
		}
	}
}
function jogar(){
	
	let atributoSelecionado = obtemAtributoSelecionado();
	let elementoResultado = document.getElementById("resultado");

	let valorCartaJogador = (cartaJogador.atributos[atributoSelecionado]);
	let valorCartaMaquina = (cartaMaquina.atributos[atributoSelecionado]);

	if (valorCartaJogador > valorCartaMaquina){
		elementoResultado.innerHTML = "YOU WIN!"
	}else if (valorCartaJogador < valorCartaMaquina){
		elementoResultado.innerHTML = "YOU LOOSE :("
	}else if (valorCartaJogador = valorCartaMaquina) {
		elementoResultado.innerHTML = "We have a tie..."
	}else{
		alert("please, pick an attribute to play");
		Location.reload();
	};

	exibirCartaMaquina();
}
function exibirCartaJogador(){
	let divCartaJogador = document.getElementById("carta-jogador");
	divCartaJogador.style.backgroundImage=`url(${cartaJogador.imagem})`;

	let moldura = '<img src="https://www.alura.com.br/assets/img/imersoes/dev-2021/card-super-trunfo-transparent-ajustado.png" style=" width: inherit; height: inherit; position: absolute;">';
	let tagHTML = "<div id='opcoes' class='carta-status'>";

	let opcoesTexto = "";
	for(let atributos in cartaJogador.atributos){
		opcoesTexto += "<input type='radio' name='atributos' required value='" +atributos + "'>" + atributos + " " + cartaJogador.atributos[atributos] + "<br>";
	}

	let nome = `<p class="carta-subtitle">${cartaJogador.nome}</p>"`;

	divCartaJogador.innerHTML = moldura + nome + tagHTML + opcoesTexto + "</div>";
}
